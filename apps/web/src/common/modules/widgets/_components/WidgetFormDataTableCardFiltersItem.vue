            }
            .operator-dropdown {
                @apply relative;
                .operator-button {
                    @apply flex items-center cursor-pointer gap-1;
                    .selected {
                        @apply text-label-md text-gray-800;
                    }
                }
                .operator-menu {
                    @apply absolute;
                    top: 100%;
                    left: 0;
                    z-index: 10;
                    width: max-content;
                }
            }
        }
    }
}
