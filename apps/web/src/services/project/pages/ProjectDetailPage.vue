    height: 100%;
    margin-top: -0.25rem;
}
.top-wrapper {
    @apply mb-8 flex flex-wrap items-center;
    .button-wrapper {
        @apply inline-flex items-center;
        .badge-content-wrapper {
            @apply text-gray-900;
            display: flex;
            align-content: center;
            gap: 0.25rem;
        }
    }
}
